     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $profils = User::findOrFail($id);
        $input = $request->all();
        $profils->fill($input)->save();
        $request->session()->flash('flash_message', 'Profile successfully update!');
        return redirect()->back();
    }

